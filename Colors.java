class Colors
 {
    public static void main(String[] args) {
        int i = 1;
        do {
            if (i == 1) System.out.println("Black");
            else if (i == 2) System.out.println("Purple");
            else if (i == 3) System.out.println("Pink");
            else if (i == 4) System.out.println("Sky Blue");
            else if (i == 5) System.out.println("Orange");
            i++;
        } while (i <= 5);
    }
}
